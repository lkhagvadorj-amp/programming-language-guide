package main

func main() {
	// int型変数 n に初期値として100を入れる
	n := 100
	// nから10を引いてnに代入しましょう
	n -= 10

	println(n)

	// int型変数 m に初期値として10を入れる
	m := 10
	// mに1を足してmに代入しましょう
	m++

	println(m)

	// string型変数 message に初期値として10を入れる
	message := "Hello World"
	// mに1を足してmに代入しましょう
	message += " Go"

	println(message)

}
